uint8_t crc_compute_crc7(uint8_t crc, uint8_t *data, uint32_t len){
    while(len){
        crc=crc7_table[crc^*data];
        data++;
        len--;
    }
    return (crc>>1);
}

uint16_t crc_compute_ccitt(uint16_t crc, uint8_t *data, uint32_t len){
    while(len){
        crc=(crc >> 8) ^ crc_ccitt_table[(crc ^ *data) & 0xff];
        data++;
        len--;
    }
    return crc;
}


uint16_t crcTable[256];
uint8_t crc7Table[256];

//good for CRC16 bit polys
void crc16_init(void){
    uint16_t res=0;
    uint16_t div=0;
    uint8_t i;
    for(div=0;div<256;++div){
        res=div<<8;
        for(i=8;i>0;--i){
            if(res&(0x8000)){
                res=(res<<1)^0x1021;
            }
            else{
                res=(res<<1);
            }
        }
        crcTable[div]=res;
    }
}

void crc7_init(void){
    uint16_t res=0;
    uint16_t div=0;
    uint8_t i; 

    for(div=0;div<256;++div){
        if(div&0x80){
            res=div^0x89;
        }
        else{
            res=div;
        }
        for(i=8;i>0;--i){
            res=res<<1;
            if(res&0x80){
                    res=res^0x89;
            }
        }
        crc7Table[div]=res;
    }
}

