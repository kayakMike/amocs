uint16_t crc_ccitt(uint8_t *msg, uint32_t len){
    uint16_t i;
    uint16_t res=0;
    while(len--){
        i=*msg^(res>>8);
        res=crcTable[i]^(res<<8);
        msg++;
    }
    return res;
}

uint8_t crc7(uint8_t *msg, uint32_t len){
    uint16_t i;
    uint8_t res=0;
    while(len--){
        i=*msg^res;
        res=crc7Table[i]^res;
        msg++;
    }
    return res;
}



